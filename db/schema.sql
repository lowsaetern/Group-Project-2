CREATE TABLE Cities (
    id INTEGER AUTO_INCREMENT PRIMARY KEY,
    city_name VARCHAR(30) NOT NULL,
    food_cost VARCHAR(30) NOT NULL,
    trans_cost VARCHAR(30) NOT NULL,
    rent_cost VARCHAR(30) NOT NULL,
    util_cost VARCHAR(30) NOT NULL,
    entertain_cost VARCHAR(30) NOT NULL,
    median_salary VARCHAR(30) NOT NULL
    );